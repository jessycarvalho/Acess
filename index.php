    <div class="container" id="wcag">
      <br><br><br><br><h1>WCAG</h1><br>
      
      <h2>Abreviatura de Web Content Accessibility Guideline, Guia da Acessibilidade no conteúdo Web em tradução livre.</h2><br><br>
      <h2>É um conjunto de requisitos para que um website seja suficientemente acessível para todo tipo de usuário.</h2><br><br>
      <h2>Esses requisitos são administrados e desenvolvidos pela W3C (Wordwide Web Consortium)</h2><br><br><br>

      <h1>Princípios:</h1>
      <br><br>
      <h2>Perceptível:</h2><br>
      <h2>Pode ser percebido de várias maneiras: texto, áudio, imagem...</h2>
      <br><br>
      <h2>Operável:</h2><br>
      <h2>Operável por vários disposivos além do mouse.</h2>
      <br><br>
      <h2>Compreensível:</h2><br>
      <h2>Deve-se pensar se todos os usuários podem entender o conteúo.</h2>
      <br><br>
      <h2>Robusto:</h2><br>
      <h2>Pode ser entendido por várias tecnologias, como o sistema operacional DosVox, adaptado para pessoas que não podem ver.</h2>
      <br><br><br>
      <h1>Níveis de Recomendações:</h1>
      <br><br>
      <h2>A</h2><br>
      <h2>Requisitos que se não seguidos, ninguém conseguirá usar o site.</h2>
      <br><br>
      <h2>AA</h2><br>
      <h2>Requisitos que se não seguidos, o site terá difícil uso</h2>
      <br><br>
      <h2>AAA</h2><br>
      <h2>Ao segir esses requisitos, o site terá uma usabilidade ainda melhor.</h2>
      <br><br><br>
      <h2>Uma maneira de ver se o site segue a WCAG é visitando o site da:<a href="https://webaim.org/resources">WebAIM</a></h2>
